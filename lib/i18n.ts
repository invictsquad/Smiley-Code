export const t = (key: string, lang: Language, options?: Record<string, string>): string => {
  const keys = key.split('.');
  let result: any = translations[lang];
  try {
    for (const k of keys) {
      result = result[k];
    }
    if (typeof result === 'string') {
      if (options) {
        return Object.entries(options).reduce((acc, [optionKey, value]) => {
          return acc.replace(`{${optionKey}}`, value);
        }, result);
      }
      return result;
    }
    return key; // Fallback if key path is valid but not a string
  } catch (e) {
    return key; // Fallback if key path is invalid
  }
};